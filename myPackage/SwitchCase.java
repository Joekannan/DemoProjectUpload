package myPackage;

public class SwitchCase {

	public static void main(String[] agrs) {

		String month = "dggsdgsd";
		
		switch(month) 
		{
		
			case "Jan":
				System.out.println("1st month in a calendar year");
				break;
			case "Feb":
				System.out.println("2nd month in a calendar year");
				break;
			case "Mar":
				System.out.println("3rd month in a calendar year");
				break;
			case "Apr":
				System.out.println("4th month in a calendar year");
				break;
			case "May":
				System.out.println("5th month in a calendar year");
				break;
			case "Jun":
				System.out.println("6th month in a calendar year");
				break;
			case "Jul":
				System.out.println("7th month in a calendar year");
				break;
			case "Aug":
				System.out.println("8th month in a calendar year");
				break;
			case "Sep":
				System.out.println("9th month in a calendar year");
				break;
			default:
				System.out.println("This is not a month in a calendar year");
				break;
		}
		
	}
	
}
